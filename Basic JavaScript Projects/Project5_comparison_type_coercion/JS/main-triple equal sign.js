// Using the === operator
var comparison1 = 5 === 5;            // true because both value and data type are the same
var comparison2 = "hello" === "hello"; // true because both value and data type are the same
var comparison3 = 5 === "5";          // false because data types are different
var comparison4 = 5 === 10;           // false because values are different
var comparison5 = "hello" === "world";// false because values are different

// Output the results
document.write("Comparison 1: " + comparison1 + "<br>");
document.write("Comparison 2: " + comparison2 + "<br>");
document.write("Comparison 3: " + comparison3 + "<br>");
document.write("Comparison 4: " + comparison4 + "<br>");
document.write("Comparison 5: " + comparison5 + "<br>");
