// Using the NOT (!) operator
var notTrue = !(5 > 2);  // false because the condition (5 > 2) is true, and ! flips it to false
var notFalse = !(5 < 2); // true because the condition (5 < 2) is false, and ! flips it to true

// Output the results
document.write("NOT True: " + notTrue + "<br>");
document.write("NOT False: " + notFalse + "<br>");